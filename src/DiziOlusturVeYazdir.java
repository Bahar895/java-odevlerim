public class DiziOlusturVeYazdir {
    public static void main(String[] args) {
        // 5 elemanlı int dizisi tanımlıyoruz
        int[] sayilar = {10,20,30,40,50};
        // For döngüsü ile dizinin tüm elemanlarını yazdırıyoruz
        for (int i =0; i < sayilar.length; i++) {
            System.out.println("Dizinin" + i + ".elemanı:"+ sayilar[i]);
}
}
}
